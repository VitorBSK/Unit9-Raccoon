/**
 * Helpers for normalizing or parsing language model outputs.
 */
export function extractJsonBlockFromText(text: string): string | null {
  const start = text.indexOf("{");
  const end = text.lastIndexOf("}");
  if (start === -1 || end === -1 || end <= start) {
    return null;
  }
  return text.slice(start, end + 1);
}
